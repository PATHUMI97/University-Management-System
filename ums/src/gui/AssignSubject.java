      public void combobox1() {
        String query1 = "SELECT * FROM `course`";

        try {

            PreparedStatement psm1 = conn.prepareStatement(query1);

            ResultSet rs1 = psm1.executeQuery();

            while (rs1.next()) {
                String name = rs1.getString("name");
                jComboBox1.addItem(name);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
      
